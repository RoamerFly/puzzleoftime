// ============================================================
// 三维选择函数
// ============================================================

function echoFor(elderId: string, consequence: string, insightLevel: number): EchoTriplet {
  const elderEchoes = ECHOES[elderId];
  if (!elderEchoes) {
    return { saw: '', did: '', felt: '' };
  }
  const conseqEchoes = elderEchoes[consequence];
  if (!conseqEchoes) {
    return { saw: '', did: '', felt: '' };
  }
  return conseqEchoes[insightLevel] ?? conseqEchoes[0] ?? { saw: '', did: '', felt: '' };
}

// ============================================================
// 反思文本
// ============================================================

function reflectionFor(successCount: number, failureCount: number, partialCount: number): string {
  if (failureCount >= 2) {
    return '交接记录可以只写"已告知"，但下一次，也许可以换个方式再问一遍。';
  }
  if (failureCount === 1) {
    return '有的人说了"没事"，手还在抖；有的人对着手机发呆，却说自己"只是在休息"。看见那些没说出口的——有时候比完成流程更重要。';
  }
  if (successCount >= 2) {
    return '上午巡房结束了。交接本上有数字、有观察、有需要继续关注的事项——每个人的名字后面，都多了一行有人真正停下来的时刻。';
  }
  if (partialCount >= 2) {
    return '流程全部完成了。那些没写进交接本的事情，也许要等下一次——等多停下来的几分钟，等一句没被催着说完的话。';
  }
  return '巡房结束了。他们不需要你什么都做到。有时候你多坐了一会，他们就不再把自己的手藏起来了。';
}

// ============================================================
// 构建结局场景
// ============================================================

export function buildEndingScene(eventResults: EventResult[]): EndingScene {
  const count = eventResults.length;

  if (count === 0) {
    return {
      label: '刚到',
      subtitle: '你还没来得及走进任何一扇门。也许下次。',
      echoes: [],
      reflection: '',
    };
  }

  const successCount = eventResults.filter((r) => r.consequence === 'success').length;
  const partialCount = eventResults.filter((r) => r.consequence === 'partial').length;
  const failureCount = eventResults.filter((r) => r.consequence === 'failure').length;

  let label: string;
  let subtitle: string;

  if (failureCount >= 2) {
    label = '流程已经结束';
    subtitle = '事件处理完了。三扇门里的声音，有些被听到，有些没有。';
  } else if (failureCount === 1) {
    label = '有些话没有被写下';
    subtitle = '一扇门没有打开。那个没被走近的人，今天还是一个人待着。';
  } else if (successCount === count) {
    label = '被记录下来的细节';
    subtitle = '交接记录里，每个名字后面都多了一行——不只是数字。';
  } else if (successCount >= 2) {
    label = '有人停留的上午';
    subtitle = '三扇门，有两扇被打开得足够久。那些没说出口的话，有些还是被听见了。';
  } else {
    label = '记录之外仍有空白';
    subtitle = '该做的事做完了。那些没写进交接本的事情，也许要等下一次。';
  }

  const echoes: ElderEcho[] = eventResults.map((r) => {
    const insightLevel = (r.insightLevel ?? 0) as InsightLevel;
    const triplet = echoFor(r.elderId, r.consequence, insightLevel);
    return {
      elderId: r.elderId,
      elderName: r.elderName,
      consequence: r.consequence,
      insightLevel,
      saw: triplet.saw,
      did: triplet.did,
      felt: triplet.felt,
      echo: triplet.saw ? `${triplet.saw} ${triplet.did} ${triplet.felt}` : '今天有人来过。',
    };
  });

  return {
    label,
    subtitle,
    echoes,
    reflection: reflectionFor(successCount, failureCount, partialCount),
  };
}
