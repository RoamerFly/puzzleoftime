import type { EventResult } from '../data/caregiverState';
import type { InsightLevel } from '../logic/insightRules';

export interface ElderEcho {
  elderId: string;
  elderName: string;
  consequence: string;
  insightLevel: number;
  /** 三段式：看到什么 */
  saw: string;
  /** 三段式：怎么处理 */
  did: string;
  /** 三段式：老人的主观感受（护工观察视角） */
  felt: string;
  /** @deprecated 旧版单字段，保留兼容 */
  echo: string;
}

export interface EndingScene {
  label: string;
  subtitle: string;
  echoes: ElderEcho[];
  reflection: string;
}

